package exercicio41a60.exercicio_43;

public class Peca {

    private int codigo;
    private double preco;

    public Peca(int codigo, double preco) {
        this.codigo = codigo;
        this.preco = preco;
    }

    public int getCodigo() {
        return codigo;
    }


    public double getPreco() {
        return preco;
    }

}
